package Subject.Models;

public class SubjectTerm {

	private int id;
	private String name;
	
	public SubjectTerm(String name,int id){
		this.id = id;
		this.name = name;
	}
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
}
